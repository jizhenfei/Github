
public class ServletInstance {
	String name = null;
	String url = null;
	
	public ServletInstance(){
	}
	
	String GetName(){
		return name;	
	}
	
	String GetUrl(){
		return url;
	}
	
	public void setName(String name){
		this.name = name;
	}
	
	public void setUrl(String url){
		this.url = url;
	}
	
}
