	@Override
	public String getParameter(String name) {
		// TODO Auto-generated method stub
		String value="";
		if(method.equals("GET"))
		{		
			String queryresource=null;
			int index1=reqString.indexOf("?");
			int index2=reqString.indexOf(" HTTP");
			if(index1!=-1){
				queryresource = reqString.substring(index1+1,index2);
				String res[] = queryresource.split("&");
				for(int i=0;i<res.length;i++){
					if(res[i].contains(name)){
						if(!value.isEmpty()){
							value+="&";
						}
						value+=res[i].substring(name.length()+1);
					}
				}
			}
			return value;
		}
		if(method.equals("POST"))
		{
			String queryresource=null;
			int index1=reqString.indexOf("\r\n\r\n");
			if(index1!=-1){
				queryresource = reqString.substring(index1+4);
				String res[] = queryresource.split("&");
				for(int i=0;i<res.length;i++){
					if(res[i].contains(name)){
						if(!value.isEmpty()){
							value+="&";
						}
						value+=res[i].substring(name.length()+1);
					}
				}
			}
			return value;
		}
		return null;
	}

